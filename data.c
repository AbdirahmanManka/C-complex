#include <stdio.h>

int main() {
    int a = 10;          // Integer
    float b = 5.75;      // Floating point number
    double c = 3.141592; // Double (higher precision)
    char d = 'X';        // Character

    printf("Integer: %d\n", a);
    printf("Float: %f\n", b);
    printf("Double: %lf\n", c);
    printf("Character: %c\n", d);

    return 0;
}
